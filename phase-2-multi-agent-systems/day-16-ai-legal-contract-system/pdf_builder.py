# ---------------------------------------------------------------------------
# Helper: header/footer on every page
# ---------------------------------------------------------------------------

def _header_footer(canvas, doc):
    canvas.saveState()
    w, h = A4

    # Header bar
    canvas.setFillColor(NAVY)
    canvas.rect(0, h - 20 * mm, w, 20 * mm, fill=1, stroke=0)
    canvas.setFillColor(GOLD)
    canvas.rect(0, h - 22 * mm, w, 2 * mm, fill=1, stroke=0)

    canvas.setFillColor(colors.white)
    canvas.setFont("Helvetica-Bold", 9)
    canvas.drawString(15 * mm, h - 13 * mm, "⚖  AI Legal Contract System")
    canvas.setFont("Helvetica", 8)
    canvas.drawRightString(w - 15 * mm, h - 13 * mm, f"CONFIDENTIAL  |  Page {doc.page}")

    # Footer
    canvas.setFillColor(BORDER)
    canvas.rect(0, 0, w, 8 * mm, fill=1, stroke=0)
    canvas.setFillColor(colors.HexColor("#64748B"))
    canvas.setFont("Helvetica", 7)
    canvas.drawString(15 * mm, 3 * mm, "Generated by AI Legal Contract System — Day 16 | 30 Days of AI Agents by Maryam Mumtaz")
    canvas.drawRightString(w - 15 * mm, 3 * mm, datetime.now().strftime("%Y-%m-%d %H:%M"))

    canvas.restoreState()


