NAVY     = colors.HexColor("#0F172A")
GOLD     = colors.HexColor("#D4AF37")
BLUE     = colors.HexColor("#1E40AF")
LIGHT_BG = colors.HexColor("#F8FAFC")
BORDER   = colors.HexColor("#CBD5E1")
RED_RISK = colors.HexColor("#DC2626")
YEL_RISK = colors.HexColor("#D97706")
GRN_RISK = colors.HexColor("#16A34A")

# Risk color thresholds
def risk_color(severity: int) -> colors.Color:
    if severity >= 7:
        return RED_RISK
    if severity >= 4:
        return YEL_RISK
    return GRN_RISK


